const dicts: Record<Lang, Dict> = { nl, en }

export function translate(lang: Lang, key: string, ...args: any[]): string {
  const dict = dicts[lang]
  const val = dict[key]
  if (val === undefined) return key
  if (typeof val === 'function') return (val as (...a: any[]) => string)(...args)
  if (Array.isArray(val)) return val.join(', ')
  if (typeof val === 'object') return key  // maps — use translateMap instead
  return String(val)
}

/** Returns the array value for list keys (e.g. callOutcomes, lostReasons) */
export function translateArr(lang: Lang, key: string): string[] {
  const dict = dicts[lang]
  const val = dict[key]
  if (Array.isArray(val)) return val as string[]
  return []
}

/** Returns the record value for map keys (e.g. adQTypeLabels) */
export function translateMap(lang: Lang, key: string): Record<string, string> {
  const dict = dicts[lang]
  const val = dict[key]
  if (val && typeof val === 'object' && !Array.isArray(val) && typeof val !== 'function') {
    return val as Record<string, string>
  }
  return {}
}
